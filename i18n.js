(function () {
  const nodes = [...document.querySelectorAll('[data-i18n]')];
  const orig = new Map();
  nodes.forEach(n => orig.set(n, n.innerHTML));
  const phNodes = [...document.querySelectorAll('[data-i18n-ph]')];
  const origPh = new Map();
  phNodes.forEach(n => origPh.set(n, n.getAttribute('placeholder')));

  const current = document.querySelector('.lang-current');
  const menuBtns = [...document.querySelectorAll('.lang-menu button')];

  function setLang(lang) {
    if (lang !== 'es' && !I18N[lang]) lang = 'es';
    document.documentElement.lang = lang;
    nodes.forEach(n => {
      const key = n.getAttribute('data-i18n');
      n.innerHTML = (lang === 'es') ? orig.get(n) : (I18N[lang][key] ?? orig.get(n));
    });
    phNodes.forEach(n => {
      const key = n.getAttribute('data-i18n-ph');
      n.setAttribute('placeholder', (lang === 'es') ? origPh.get(n) : (I18N[lang][key] ?? origPh.get(n)));
    });
    if (current) current.textContent = lang.toUpperCase();
    menuBtns.forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
    try { localStorage.setItem('lang', lang); } catch (e) {}
  }

  // Selector
  const wrap = document.querySelector('.lang-switch');
  const btn = document.querySelector('.lang-btn');
  if (btn && wrap) {
    btn.addEventListener('click', e => {
      e.stopPropagation();
      const open = wrap.classList.toggle('open');
      btn.setAttribute('aria-expanded', String(open));
    });
    document.addEventListener('click', () => {
      wrap.classList.remove('open');
      btn.setAttribute('aria-expanded', 'false');
    });
    menuBtns.forEach(b => b.addEventListener('click', () => {
      setLang(b.dataset.lang);
      wrap.classList.remove('open');
      btn.setAttribute('aria-expanded', 'false');
    }));
  }

  let saved = 'es';
  try { saved = localStorage.getItem('lang') || 'es'; } catch (e) {}
  setLang(saved);
})();
